import { expect, it } from "vitest";
import { encrypt, decrypt } from "./aes";
import { Buffer } from "node:buffer";

const plain = Buffer.from([
  0x54, 0x77, 0x6f, 0x20, 0x4f, 0x6e, 0x65, 0x20, 0x4e, 0x69, 0x6e, 0x65, 0x20,
  0x54, 0x77, 0x6f,
]);
const encrypted = Buffer.from([
  0x29, 0xc3, 0x50, 0x5f, 0x57, 0x14, 0x20, 0xf6, 0x40, 0x22, 0x99, 0xb3, 0x1a,
  0x02, 0xd7, 0x3a,
]);

const key = Buffer.from([
  0x54, 0x68, 0x61, 0x74, 0x73, 0x20, 0x6d, 0x79, 0x20, 0x4b, 0x75, 0x6e, 0x67,
  0x20, 0x46, 0x75,
]);

it("Encrypt a 128bit data", () => {
  const expected = encrypted;
  const actual = encrypt(plain, key);
  expect(actual).toEqual(expected);
});

it("Decrypt a 128bit data", () => {
  const expected = plain;
  const actual = decrypt(encrypted, key);
  expect(actual).toEqual(expected);
});
