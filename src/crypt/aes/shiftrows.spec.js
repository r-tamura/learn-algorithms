import test from "ava"
import { shiftRows } from "./shiftrows"

test("shiftRows", t => {
  const state    = [
    [0x63, 0xeb, 0x9f, 0xa0],
    [0xc0, 0x2f, 0x93, 0x92],
    [0xab, 0x30, 0xaf, 0xc7],
    [0x20, 0xcb, 0x2b, 0xa2],
  ]
  const expected = [
    [0x63, 0xeb, 0x9f, 0xa0],
    [0x2f, 0x93, 0x92, 0xc0],
    [0xaf, 0xc7, 0xab, 0x30],
    [0xa2, 0x20, 0xcb, 0x2b],
  ]
  const actual   = shiftRows(state)
  t.deepEqual(actual, expected)
})
