import { expect, it } from "vitest";
import { shiftRows, shiftRowsInverse } from "./shiftrows";

it("shiftRows", () => {
  const state = [
    [0x63, 0xeb, 0x9f, 0xa0],
    [0xc0, 0x2f, 0x93, 0x92],
    [0xab, 0x30, 0xaf, 0xc7],
    [0x20, 0xcb, 0x2b, 0xa2],
  ];
  const expected = [
    [0x63, 0xeb, 0x9f, 0xa0],
    [0x2f, 0x93, 0x92, 0xc0],
    [0xaf, 0xc7, 0xab, 0x30],
    [0xa2, 0x20, 0xcb, 0x2b],
  ];
  const actual = shiftRows(state);
  expect(actual).toEqual(expected);
});

it("shiftRows Inverse", () => {
  const state = [
    [0x63, 0xeb, 0x9f, 0xa0],
    [0x2f, 0x93, 0x92, 0xc0],
    [0xaf, 0xc7, 0xab, 0x30],
    [0xa2, 0x20, 0xcb, 0x2b],
  ];
  const expected = [
    [0x63, 0xeb, 0x9f, 0xa0],
    [0xc0, 0x2f, 0x93, 0x92],
    [0xab, 0x30, 0xaf, 0xc7],
    [0x20, 0xcb, 0x2b, 0xa2],
  ];
  const actual = shiftRowsInverse(state);
  expect(actual).toEqual(expected);
});
