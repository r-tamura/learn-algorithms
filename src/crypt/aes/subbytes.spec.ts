import { expect, it } from "vitest";
import { subBytes, subBytesInverse } from "./subbytes";

it("Substitution Bytes", () => {
  const state = [
    [0x00, 0x3c, 0x6e, 0x47],
    [0x1f, 0x4e, 0x22, 0x74],
    [0x0e, 0x08, 0x1b, 0x31],
    [0x54, 0x59, 0x0b, 0x1a],
  ];
  const expected = [
    [0x63, 0xeb, 0x9f, 0xa0],
    [0xc0, 0x2f, 0x93, 0x92],
    [0xab, 0x30, 0xaf, 0xc7],
    [0x20, 0xcb, 0x2b, 0xa2],
  ];
  const actual = subBytes(state);
  expect(actual).toEqual(expected);
});

it("Substitution Bytes Inverse", () => {
  const state = [
    [0x63, 0xeb, 0x9f, 0xa0],
    [0xc0, 0x2f, 0x93, 0x92],
    [0xab, 0x30, 0xaf, 0xc7],
    [0x20, 0xcb, 0x2b, 0xa2],
  ];
  const expected = [
    [0x00, 0x3c, 0x6e, 0x47],
    [0x1f, 0x4e, 0x22, 0x74],
    [0x0e, 0x08, 0x1b, 0x31],
    [0x54, 0x59, 0x0b, 0x1a],
  ];
  const actual = subBytesInverse(state);
  expect(actual).toEqual(expected);
});
